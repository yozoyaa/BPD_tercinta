def get_grade(nilai_akhir):
    if nilai_akhir >= 85:
        return 'A'
    elif nilai_akhir >= 80:
        return 'A'
    elif nilai_akhir >= 75:
        return 'B+'
    elif nilai_akhir >= 70:
        return 'B'
    elif nilai_akhir >= 65:
        return 'C'
    else:
        return 'D'
